                <h1>About Saferlanes</h1>
                <p>Saferlanes is a web application for tracking a driver' s reputation on the road.</p>
                <p>
                    Saferlanes is an experiment in crowd sourcing and is meant to demonstrate how the Web can assist us in dealing
                    with common problems. In this case reckless driving.
                </p>
                <p>
                <h5>How to:</h5>
                <p>Let's say you see someone breaking a red light let's call him abc123:</p>
                <ul>
                    <li>
                        <p>First <a href="/search">search</a> the database for that plate number.</p>
                    </li>
                    <li>
                    <p>If it's not entered you <a href="/post">post</a> it. After that you should see the profile of the plate number.</p>
                    </li>
                    <li>
                        <p>The <a href="/abc123">profile</a> shows the number of +1 and -1 the plate number has accumulated so far. Click on the + or - to cast your vote.</p>
                    </li>
                </ul>
                <p>
                    That's it! That is all there is to it!
                </p>
